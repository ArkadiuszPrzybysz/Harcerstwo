      <p>Została powołana 19 marca 2003 r. Założycielami stowarzyszenia byli: Urszula Bondarowicz,
Henryk Helwing, Daniela Kołdyk, Zdzisław Mizerski, Andrzej Pieniężny, Dorota Piechota,
